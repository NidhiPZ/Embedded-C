#include<stdio.h>
int main()
{
    // Create variables
    int myNum = 5;             // Integer number
    float myFloatNum = 5.99;   // Floating point number
    char myLetter = 'N';       // Character

    // Print variables
    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
}